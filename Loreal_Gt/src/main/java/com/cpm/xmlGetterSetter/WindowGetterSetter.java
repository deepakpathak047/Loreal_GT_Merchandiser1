public class WindowGetterSetter {

    String window, window_cd, planogram;

    public String getWindow() {
        return window;
    }

    public void setWindow(String window) {
        this.window = window;
    }

    public String getWindow_cd() {
        return window_cd;
    }

    public void setWindow_cd(String window_cd) {
        this.window_cd = window_cd;
    }

    public String getPlanogram() {
        return planogram;
    }

    public void setPlanogram(String planogram) {
        this.planogram = planogram;
    }
}
